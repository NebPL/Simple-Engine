package util

fun white(): Int {
    return 0xFFFFFF
}

fun black(): Int {
    return 0x000000
}

fun red(): Int {
    return 0xFF0000
}

fun green(): Int {
    return 0x00FF00
}

fun blue(): Int {
    return 0x0000FF
}

fun yellow(): Int {
    return 0xFFFF00
}

fun cyan(): Int {
    return 0x00FFFF
}

fun magenta(): Int {
    return 0xFF00FF
}

fun gray(): Int {
    return 0x808080
}
